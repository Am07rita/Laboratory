//Wap to print hello 5 times

package basic;
public class First {

	public static void main(String[] args) {
	int i;//counter value	
    for(i=0; i<=4; i++) //increment
    {
       System.out.println("Hello");	
    }
    System.out.println();
    for(i=5; i>0; i--) //decrement
    {
       System.out.println("world");	
    }	
	}

}
